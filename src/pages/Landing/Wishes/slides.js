function shuffle(arr) {
	for (let i = arr.length - 1; i > 0; --i) {
		const pos = Math.floor(Math.random() * (i + 1));
		const t = arr[pos];
		arr[pos] = arr[i];
		arr[i] = t;
	}
	return arr;
}


